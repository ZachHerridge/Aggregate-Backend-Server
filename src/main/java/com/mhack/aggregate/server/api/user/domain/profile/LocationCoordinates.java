package com.mhack.aggregate.server.api.user.domain.profile;

class LocationCoordinates {
    double x;
    double y;

    public LocationCoordinates(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
